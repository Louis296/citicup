package com.louis296.citicup.model;


public class Team {

  private String name;
  private double videoView;
  private String likes;
  private String comments;
  private String forwardingNumbers;
  private long sponsorSize;
  private long searchIndex;
  private long developmentPotentialPoints;
  private long comprehensiveStrengthPoints;
  private String clubName;
  private String gameName;


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }


  public double getVideoView() {
    return videoView;
  }

  public void setVideoView(double videoView) {
    this.videoView = videoView;
  }


  public String getLikes() {
    return likes;
  }

  public void setLikes(String likes) {
    this.likes = likes;
  }


  public String getComments() {
    return comments;
  }

  public void setComments(String comments) {
    this.comments = comments;
  }


  public String getForwardingNumbers() {
    return forwardingNumbers;
  }

  public void setForwardingNumbers(String forwardingNumbers) {
    this.forwardingNumbers = forwardingNumbers;
  }


  public long getSponsorSize() {
    return sponsorSize;
  }

  public void setSponsorSize(long sponsorSize) {
    this.sponsorSize = sponsorSize;
  }


  public long getSearchIndex() {
    return searchIndex;
  }

  public void setSearchIndex(long searchIndex) {
    this.searchIndex = searchIndex;
  }


  public long getDevelopmentPotentialPoints() {
    return developmentPotentialPoints;
  }

  public void setDevelopmentPotentialPoints(long developmentPotentialPoints) {
    this.developmentPotentialPoints = developmentPotentialPoints;
  }


  public long getComprehensiveStrengthPoints() {
    return comprehensiveStrengthPoints;
  }

  public void setComprehensiveStrengthPoints(long comprehensiveStrengthPoints) {
    this.comprehensiveStrengthPoints = comprehensiveStrengthPoints;
  }


  public String getClubName() {
    return clubName;
  }

  public void setClubName(String clubName) {
    this.clubName = clubName;
  }


  public String getGameName() {
    return gameName;
  }

  public void setGameName(String gameName) {
    this.gameName = gameName;
  }

}
